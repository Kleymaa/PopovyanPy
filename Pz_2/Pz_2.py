#Постановка задачи:
#Дано целое число, больше 999. Используя одну операцию деления нацело и одну операцию взятия остатка от деления, найти цифру, соответствующую разряду тысяч в записи этого числа.


try:
    #Вводим число и сохраняем его в переменной x
    x = int(input("Введите число: "))
    #Если введённое число больше или равно 1000
    if x >= 1000:
        #Выполняем целочисленное деление числа на 1000, чтобы получить разряд тысяч
        y = x // 1000
        #Делим на 10, чтобы извлечь последнюю цифру в разряде 1000
        n = y % 10
        #Выводим полученную цифру
        print(n)
    else:
        #Если число меньше 1000, выводим сообщение об ошибке
        print("Ошибка: введённое число должно быть больше или равно 1000.")
except ValueError:
    #Сообщение об ошибке, если введено некорректное значение
    print("Ошибка: введено некорректное значение.")