try:
    # Пробуем получить целое число от пользователя
    x = int(input("Введите число: "))

    # Если введённое число больше или равно 1000
    if x >= 1000:
        # Выполняем целочисленное деление числа на 1000, чтобы получить разряд тысяч
        y = x // 1000

        # Выводим полученную цифру
        print(y)
    else:
        # Если число меньше 1000, выводим сообщение об ошибке
        print("Ошибка: введённое число должно быть больше или равно 1000.")
except ValueError:
    # Если пользователь ввёл некорректное значение (например, строку), выводим сообщение об ошибке
    print("Ошибка: введено некорректное значение.")
