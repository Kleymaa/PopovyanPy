#Постановка задачи:
#Дан целочисленный список размера N, не содержащий одинаковых чисел.
#Проверить, образуют ли его элементы арифметическую прогрессию. Если образуют,
#то вывести разность прогрессии, если нет — вывести 0.


def my_def(lst):
    n = len(lst)
    if n < 2:
        #Список меньше двух элементов, не может быть прогрессии
        return 0

    #Находим разность между первыми двумя элементами
    diff = lst[1] - lst[0]

    #Проверяем, что все остальные пары имеют такую же разность
    for i in range(2, n):
        if lst[i] - lst[i - 1] != diff:
            # Прогрессия нарушена
            return 0

    #Все условия выполнены, возвращаем разность прогрессии
    return diff


#Вывод: 2
lst = [2, 4, 6, 8, 10]
print(my_def(lst))

lst = [1, 3, 5, 9]
#Вывод: 0
print(my_def(lst))
